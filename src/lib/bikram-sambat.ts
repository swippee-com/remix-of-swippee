// Reference date: BS 2000/01/01 = AD 1943/04/14
const BS_YEAR_START = 2000;
const REF_AD = new Date(1943, 3, 14); // April 14, 1943

const BS_MONTHS_NE = [
  "बैशाख", "जेष्ठ", "असार", "श्रावण", "भदौ", "असोज",
  "कार्तिक", "मंसिर", "पौष", "माघ", "फाल्गुन", "चैत्र",
];

const BS_MONTHS_EN = [
  "Baisakh", "Jestha", "Asar", "Shrawan", "Bhadra", "Asoj",
  "Kartik", "Mangsir", "Poush", "Magh", "Falgun", "Chaitra",
];

const NEPALI_DIGITS = ["०", "१", "२", "३", "४", "५", "६", "७", "८", "९"];

function toNepaliDigits(num: number | string): string {
  return String(num).replace(/[0-9]/g, (d) => NEPALI_DIGITS[parseInt(d)]);
}

function totalDaysInBSYear(yearIndex: number): number {
  return BS_CALENDAR_DATA[yearIndex].reduce((a, b) => a + b, 0);
}

export interface BSDate {
  year: number;
  month: number; // 1-12
  day: number;
}

export function adToBS(adDate: Date): BSDate {
  // Use UTC date components to avoid timezone drift
  const utcDate = Date.UTC(adDate.getFullYear(), adDate.getMonth(), adDate.getDate());
  const utcRef = Date.UTC(1943, 3, 14);
  let totalDays = Math.floor((utcDate - utcRef) / (1000 * 60 * 60 * 24));

  let bsYear = BS_YEAR_START;
  let bsMonth = 0;
  let bsDay = totalDays;

  // Find BS year
  let yearIndex = 0;
  while (yearIndex < BS_CALENDAR_DATA.length && bsDay >= totalDaysInBSYear(yearIndex)) {
    bsDay -= totalDaysInBSYear(yearIndex);
    yearIndex++;
    bsYear++;
  }

  if (yearIndex >= BS_CALENDAR_DATA.length) {
    // Fallback for dates outside range
    return { year: bsYear, month: 1, day: 1 };
  }

  // Find BS month
  const months = BS_CALENDAR_DATA[yearIndex];
  for (let i = 0; i < 12; i++) {
    if (bsDay < months[i]) {
      bsMonth = i + 1;
      bsDay += 1; // 1-indexed day
      break;
    }
    bsDay -= months[i];
  }

  return { year: bsYear, month: bsMonth, day: bsDay };
}

/**
 * Format a BS date.
 * "short" → "१५ बैशाख, २०८२"
 * "long"  → "१५ बैशाख, २०८२"
 * "numeric" → "2082/01/15"
 */
export function formatBSDate(
  adDate: Date,
  style: "short" | "long" | "numeric" = "short",
  useNepaliScript = true
): string {
  const bs = adToBS(adDate);

  if (style === "numeric") {
    const m = String(bs.month).padStart(2, "0");
    const d = String(bs.day).padStart(2, "0");
    const str = `${bs.year}/${m}/${d}`;
    return useNepaliScript ? toNepaliDigits(str) : str;
  }

  const monthName = useNepaliScript ? BS_MONTHS_NE[bs.month - 1] : BS_MONTHS_EN[bs.month - 1];
  const day = useNepaliScript ? toNepaliDigits(bs.day) : String(bs.day);
  const year = useNepaliScript ? toNepaliDigits(bs.year) : String(bs.year);

  return `${day} ${monthName}, ${year}`;
}
